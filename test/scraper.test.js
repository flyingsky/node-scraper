var assert = require('assert');
var path = require('path');

var fse = require('fs-extra');

var Scraper = require('../lib/scraper');
var Parser = require('../lib/parser').Parser;
var imageDownloadProcessor = require('../lib/parser').ImageDownloadProcessor;

describe('scraper', function() {
  this.timeout(1000 * 60 * 2);

  it.only('html', function(done) {
    var parser = new Parser(path.resolve('test/simple-parser.json'));
    var scraper = new Scraper([parser, imageDownloadProcessor]);

    var downloadDir = parser.mapping.imgSrc.downloadDir;
    fse.ensureDirSync(downloadDir);

    var opts = {uri: 'http://www.jufengshang.com/Longines3756'};
    scraper.scrapePage(opts, function(err, result) {
      console.log(result);

      assert(result);
      assert(result.title);
      assert(result.imgSrc);
      assert(result.description);

      var downloadedImage = path.join(downloadDir, path.basename(result.imgSrc));

      assert(result.downloadImgSrc == downloadedImage);
      assert(fse.existsSync(downloadedImage));

      fse.removeSync(downloadDir);

      done(err);
    });
  });

  it('html with js', function(done) {
    var parser = new Parser(path.resolve('test/simple-parser.json'));
    parser.mapping.price = '.shop_s';  // this element is generated by js

    var scraper = new Scraper(parser);

    var opts = {
      uri: 'http://www.jufengshang.com/Longines3756',
      usePhantom: true
    };

    scraper.scrapePage(opts, function(err, result) {
      console.log(result);

      assert(result);
      assert(result.title);
      assert(result.imgSrc);
      assert(result.description);
      assert(result.price);

      done(err);
    });
  });
});